    private void RegistrarCortesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarCortesActionPerformed
        // TODO add your handling code here:
        //Se registra los cortes si el usuario existe o no
        if(!"".equals(NumCortes.getText()) && !"".equals(NombreUsuarioCortes.getText())) {
            if(Admin.Empleados.containsKey(NombreUsuarioCortes.getText())) {
                Admin.Empleados.get(NombreUsuarioCortes.getText()).NumCortes += Integer.parseInt(NumCortes.getText());
                Inventario.NumCortesTotal += Integer.parseInt(NumCortes.getText());
                Inventario.AgregarGastosSueldos();
                JOptionPane.showMessageDialog(null,"Cortes Registrados correctamente");
            }
            else {
                JOptionPane.showMessageDialog(null, "No se ha encontrado el usuario");
            }
        }
        else {
            JOptionPane.showMessageDialog(null,"Ingrese los datos correspondientes");
        }
    }//GEN-LAST:event_RegistrarCortesActionPerformed

    private void RegistrarComprasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarComprasActionPerformed
        // TODO add your handling code here:
        if(!"".equals(Cantidad.getText()) && !"".equals(CostoUnidad.getText())) {
            Inventario.AgregarGastosVariables(Insumo.getSelectedItem().toString(), Integer.parseInt(Cantidad.getText()), Integer.parseInt(CostoUnidad.getText()));
            JOptionPane.showMessageDialog(null,"Gastos Registrados correctamente");
        }
    }//GEN-LAST:event_RegistrarComprasActionPerformed

