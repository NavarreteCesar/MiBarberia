    private void RegistrarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarEmpleadoActionPerformed
        // TODO add your handling code here:
        //Se verifica si los campos estan vacios y si el usuario existe o no, sino pues se crea el usuario
        if(NombreCompleto.getText() != "" && NombreUsuario.getText() != "" && Contrasena.getText() != "") {
            if(Admin.Empleados.containsKey(NombreUsuario.getText())) {
                JOptionPane.showMessageDialog(null, "Nombre de usuario ya usado");
            }
            else {
                Admin.Empleados.put(NombreUsuario.getText(), new Empleado(NombreCompleto.getText(), NombreUsuario.getText(), Contrasena.getText()));
                JOptionPane.showMessageDialog(null,"Empleado Registrados correctamente");
            }
        }
    }//GEN-LAST:event_RegistrarEmpleadoActionPerformed

